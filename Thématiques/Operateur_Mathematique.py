'''
Opérations mathématiques en python, comment cela fonctionne ?
Les opérations mathématiques de base en python sont les suivantes: +, -, *, /, //, %, **.
Comment le mettre en place ?
'''

a = 50
x = 50
print (a + x) #Addition
print (a - x) #Soustraction
print (a * x) #Multiplications
print (a % x) #Modulo
print (a ** x) #Exponentiation
print (a // x) #Division entière
print (a / x) #Division