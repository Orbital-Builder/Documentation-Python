#Structure conditionnelle :

#Les structure conditionnelle sont :
# if condition:
#     instruction
# elif condition:
#     instruction
# else:

#if : si la condition est vraie, on exécute les instructions (Si)
#else : si toutes les conditions sont fausses, on exécute les instructions (Sinon)
#elif : si la condition est fausse, on teste une autre condition (Sinon SI)



#Exemple 1
a = 10
if a > 5:
    print("a est supérieur à 5")
else:
    print("a est inférieur à 5")

#Exemple 2
a = 10
if a > 5:
    print("a est supérieur à 5")

#Exemple 3
a = 10

if a > 5:
    print("a est supérieur à 5")
elif a < 5:
    print("a est inférieur à 5")
else:
    print("a est égal à 5")





